int	ft_is_prime(int nb)
{
	int	n;

	n = nb - 1;
	if (nb <= 1)
		return (0);
	else
	{
		while (n > 1)
		{
			if (nb % n == 0)
				return (0);
			n--;
		}
		return (1);
	}
}
