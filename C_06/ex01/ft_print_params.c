#include <unistd.h>

int	main(int argc, char *argv[])
{
	int		n;
	int		r;
	char	c;

	n = 1;
	r = 0;
	c = '\n';
	while (n < argc)
	{
		r = 0;
		while (argv[n][r] != '\0' )
		{
			write (1, &argv[n][r], 1);
			r++;
		}
		write (1, &c, 1);
		n++;
	}
}
